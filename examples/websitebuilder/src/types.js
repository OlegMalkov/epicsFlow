type RTPositionType = {| right: number, top: number |}
type DimensionsType = {| height: number, width: number |}
type BBoxType = {| bottom: number, left: number, right: number, top: number |}

export type {
	LTPositionType,
	RTPositionType,
	DimensionsType,
	BBoxType,
}
